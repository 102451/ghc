InfoProvEnt * lookupIPE(StgInfoTable *info)
{
    InfoProvEnt *ip, *next;
//    printf("%p\n", info);
    //printf("%p\n\n", clos);
    for (ip = IPE_LIST; ip != NULL; ip = next) {
//        printf("%p\n", ip->info);
        if (ip->info == info) {
            //printf("Found %p\n", ip->info);
            return ip;
        }
        next = ip->link;
    }
}
